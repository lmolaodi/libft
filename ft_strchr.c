#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s != '\0')
	{
		i = *s++;
		if (*s == c)
			return ((char *)s);
		if (*s == '\0')
			return (NULL);
	}
	return (0);
}
